export function AustralianStandar() {
  return (
    <>
      <p>
        According to the Australian Standard for mobile bin colours
        &#40;AS4123.7-2006&#41;, the colours used for waste disposal and
        recycling bins are:
      </p>

      <ul>
        <li>
          <strong>Red Lid</strong>: General waste that cannot be recycled or
          composted, such as cigarette butts, broken toys, cellophane, lids,
          lighters, stationery, rubber bands, and wipes.
        </li>
        <li>
          <strong>Yellow Lid</strong>: Recyclable materials, including paper,
          cartons, and cardboard.
        </li>
        <li>
          <strong>Green Lid</strong>: Food and garden organics, such as lawn
          clippings, weeds, leaves, small branches, and food waste like meat and
          fish.
        </li>
        <li>
          <strong>Blue Lid</strong>: This colour is often designated for
          recyclable materials, particularly glass and certain types of
          plastics.
        </li>
      </ul>
    </>
  );
}
